// Literals program

import java.awt.*;

public class Literals {
    public static void main(String[] args){
        int a = 555555484; // In this int litrals we can only 9 dits of value insert
        byte b = 127; // In this byte litrals we can only 3 diyts of value max(127)
        short c = 5456; // In this Litrals we can only 4 dits of valure insert
        long d = 48545445554555L; // In this Litrals we can insert more then 9 dits of value with help of L&l
        char e = 'A' ;
        float x = 5.6f ;
        double y = 4.5454 ;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(x);
        System.out.println(y);
        boolean r = true;
        boolean v = false;
        System.out.println(r);
        System.out.println(v);
}
}